import UIKit

extension UIColor {
    static var pmGreen: UIColor {
        get {
            return UIColor(red: 160/255, green: 178/255, blue: 137/255, alpha: 1)
        }
    }
    
    static var pmLightGreen: UIColor {
        get {
            return UIColor(red: 195/255, green: 204/255, blue: 184/255, alpha: 1)
        }
    }
}
